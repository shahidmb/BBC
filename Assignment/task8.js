 var num1 = prompt("Enter the First Number: ");
     num1 = Number(num1)
 
 if(num1 == 0)
 {
    document.write("Number: "+num1+" is Zero")
}
else if(num1 >= 0)
 {
    document.write("Number: "+num1+" is Positive")
}
else if(num1 <= 0)
 {
    document.write("Number: "+num1+" is Negative")
}
